// функция, которая "ждёт" указанное число миллисекунд
function wait(ms) {
  return new Promise(function(resolve) {
    setTimeout(resolve, ms);
  });
}

// async функция, которая использует await
async function run() {
  console.log("1 - start");
  await wait(2000);          // ПОДОЖДИ 2 секунды здесь
  console.log("2 - after waiting");
  console.log("3 - end");
}

run();